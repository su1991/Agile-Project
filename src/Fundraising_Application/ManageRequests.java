 public void populateArrayList2()
    {
        try
        {
        
            FileInputStream file = new FileInputStream("src\\DataBase\\Applications.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
        boolean EndOfFile = false;
        
        while (!EndOfFile){
            
            try 
            {
              Applications.add((VolunteerApplications) inputFile.readObject());
                
            }
            catch (EOFException e)
            {
                EndOfFile = true;
            }
            catch (Exception d)
            {
             // JOptionPane.showMessageDialog(null, d.getMessage());
            }
        }
        
        inputFile.close();
        }
        catch (IOException l){
            
           // JOptionPane.showMessageDialog(null, l.getMessage());
            
        }
        
        
    }
 
 
 
  public void saveApplicationsToFile(){
         
            try 
            {
              FileOutputStream file = new FileOutputStream("src\\DataBase\\Applications.dat");
              ObjectOutputStream outputFile = new ObjectOutputStream(file);
                
              
              for (int i=0; i<Applications.size(); i++){
                  
                  outputFile.writeObject(Applications.get(i));
                  
              }
              
              
             FileWriter fw =new FileWriter("src\\DataBase\\Applications.txt"); 
              
             try 
            {
                
              for (int i=0; i<Applications.size(); i++){
                   fw.write("\n" + Applications.get(i).ApplicationNumber + "\n" + Applications.get(i).Applicationstatus);
              }
                   
                  
            }
            catch (Exception f)
            {
            // JOptionPane.showMessageDialog(null, f.getMessage());
            }
                  
              
              fw.close();
              
              
              
              outputFile.close();
              JOptionPane.showMessageDialog(null, "The Application status was succefully saved");
             
            
             
            }
        
         catch (IOException e){
            
            //JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
        
    }
    
